    void camposVacios(){
        if(txtUser.getText().isEmpty() || txtPass.getText().isEmpty()){
           lblMensaje.setText("¡Ingresar usuario y constraseña!");
        }else{
            validacionLogin();
        }
    }
    
    void validacionLogin(){
        UsuarioDAO ud = new UsuarioDAO();
        usr = ud.validarUsuario(txtUser.getText(),txtPass.getText());
        if(usr.getCedula() != null && usr.getPass() != null){
            Principal p = new Principal();
            p.setVisible(true);
            dispose();
        }else{
            lblMensaje.setText("¡Usuario y/o Contraseña incorrectos!");
        }
    }
    
    
